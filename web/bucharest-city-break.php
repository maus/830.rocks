        <h2>What to see</h2>
        <h3>The Palace of Parliament</h3>
        <p>The Palace of Parliament is the world’s second-largest administrative building (after the Pentagon) and former dictator Nicolae Ceauşescu’s most infamous creation.</p>
        <p>Built in 1984 (an not yet finished), the palace is one of the most controversial buildings in Romania. It is both a symbol of power as it is a reminder of the communist era.</p>
        <p><em>Fun facts</em>: The Palace of Parliament has 1100 rooms and covers 330,000 m³. It is constructed from 700,000 tons of steel and bronze combined with 1 million m³ of marble, 3,500 tons of crystal glass and 900,000 m³ of wood. Due to its weight, the building actually sinks into the ground a little bit each year. In any case you cannot miss it. Even if you want to. The building is so massive that it's even visible from the moon.</p>
        <h3>The Old Town and Calea Victoriei</h3>
        <p>Go back in time and explore the streets of the Old Town. Here new pubs are located in centuries old buildings.</p>
        <p>Continue your walk on Victory road, where important administrative and cultural buildings meet high-end shopping stores.</p>
        <h3>Carturesti Carusel</h3>
        <p>In Bucharest's Old Town you also find one of the most beautiful bookshops. It is a six-level bookstore with an elegant and stunning interior. Enjoy a drink served in the top floor bistro, check out the quirky gifts in the shop and you will not leave disappointed.</p>
        <h3>Muzeul MaRE</h3>
        <p>Opened in 2018, this museum is a needed addition on Romania's art scene. If you want tot find aout more about the Romanian contemporary art, that does not exclude international positions, this is the place to see.</p>
